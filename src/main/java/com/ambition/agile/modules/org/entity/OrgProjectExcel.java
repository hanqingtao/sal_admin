package com.ambition.agile.modules.org.entity;
/**
 * 导出到Excel实体
 * @author ckl
 *
 */
public class OrgProjectExcel {
	
	private String areaName; //地区名
	private String orgName; //机构名
	private String scCode; //社会信用代码
	private String projectName; //项目名
	private String sn; //项目编号
	/**
	 * @return the areaName
	 */
	public String getAreaName() {
		return areaName;
	}
	/**
	 * @param areaName the areaName to set
	 */
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	/**
	 * @return the orgName
	 */
	public String getOrgName() {
		return orgName;
	}
	/**
	 * @param orgName the orgName to set
	 */
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	/**
	 * @return the scCode
	 */
	public String getScCode() {
		return scCode;
	}
	/**
	 * @param scCode the scCode to set
	 */
	public void setScCode(String scCode) {
		this.scCode = scCode;
	}
	/**
	 * @return the projectName
	 */
	public String getProjectName() {
		return projectName;
	}
	/**
	 * @param projectName the projectName to set
	 */
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	/**
	 * @return the sn
	 */
	public String getSn() {
		return sn;
	}
	/**
	 * @param sn the sn to set
	 */
	public void setSn(String sn) {
		this.sn = sn;
	}
	
	
	
}
